##반복문과 딕셔너리의 items( )를 이용하여 key와 vlaue를 동시에 출력

phone_book = {'홍길동':'010-1234-5678',
	      '강감찬':'010-1111-2222',
	      '이순신':'010-3333-4444'}

for k, v in phone_book.items( ):
    print('{}의 전화번호는 {}입니다.'.format(k, v))
